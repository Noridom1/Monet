#!/usr/bin/env bash
# Evaluate Monet-7B on VLMEvalKit benchmarks — full dataset or a subset.
#
# Subset modes (set SUBSET):
#   SUBSET=full           run the whole dataset (default)
#   SUBSET=head           first k% / first N samples (deterministic)
#   SUBSET=random         random k% / random N samples (seeded by SEED)
#   SUBSET=indices        exact rows listed in INDICES_FILE
# Size is given by exactly one of:
#   FRAC=0.1              fraction, e.g. 10%
#   N=200                 absolute count
#
# Each dataset's official TSV is restored to full before this run, then (for subset modes)
# re-subsetted, so runs are reproducible and never accumulate. Official dataset names are
# used, so official evaluators/metrics apply.
set -euo pipefail

ENV_NAME="${ENV_NAME:-monet}"
REPO_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
EVAL_DIR="${EVAL_DIR:-$REPO_DIR/VLMEvalKit}"

CONDA_BASE="${CONDA_BASE:-$HOME/miniconda3}"
source "$CONDA_BASE/etc/profile.d/conda.sh"
conda activate "$ENV_NAME"

# --- model + Monet knobs ---------------------------------------------------
MODEL_PATH="${MODEL_PATH:-$REPO_DIR/models/Monet-7B}"
# Canonicalize to an absolute path: the script later cd's into $EVAL_DIR, after which
# a relative MODEL_PATH would no longer resolve and VLMEvalKit would mistake it for a
# Hugging Face repo id and try to download it.
MODEL_PATH="$(realpath -m "$MODEL_PATH" 2>/dev/null || readlink -f "$MODEL_PATH")"
export MODEL_PATH
export LATENT_SIZE="${LATENT_SIZE:-10}"
# System prompt + token budget (run_monet.py reads these; defaults match README).
export MONET_MAX_NEW_TOKENS="${MONET_MAX_NEW_TOKENS:-2048}"
export MONET_MAX_PIXELS="${MONET_MAX_PIXELS:-1003520}"

# Optional per-sample latent intervention. The manifest is generated by
# 06_run_latent_policy_eval.sh and validated again by the model adapter.
MONET_LATENT_POLICY_MANIFEST="${MONET_LATENT_POLICY_MANIFEST:-}"
if [ -n "$MONET_LATENT_POLICY_MANIFEST" ]; then
  MONET_LATENT_POLICY_MANIFEST="$(realpath -m "$MONET_LATENT_POLICY_MANIFEST")"
  if [ ! -f "$MONET_LATENT_POLICY_MANIFEST" ]; then
    echo "[run-eval] ERROR: policy manifest not found: $MONET_LATENT_POLICY_MANIFEST" >&2
    exit 1
  fi
  export MONET_LATENT_POLICY_MANIFEST
  REUSE="${REUSE:-0}"
else
  REUSE="${REUSE:-1}"
fi
if [ "$REUSE" != "0" ] && [ "$REUSE" != "1" ]; then
  echo "[run-eval] ERROR: REUSE must be 0 or 1 (got '$REUSE')" >&2
  exit 1
fi

# --- datasets + subset selection -------------------------------------------
# Space-separated official VLMEvalKit dataset names.
DATASETS="${DATASETS:-MMBench_DEV_EN}"
SUBSET="${SUBSET:-full}"            # full | head | random | indices
FRAC="${FRAC:-}"                   # e.g. 0.1
N="${N:-}"                         # e.g. 200
SEED="${SEED:-0}"
INDICES_FILE="${INDICES_FILE:-}"

# --- judge (README: replace exact match with an API judge) -----------------
# Set JUDGE to an API model name (e.g. gpt-4o-mini, or a DeepSeek/Gemini-compatible
# model served via JUDGE_BASE_URL + JUDGE_KEY). Leave empty to use VLMEvalKit defaults.
JUDGE="${JUDGE:-}"
JUDGE_BASE_URL="${JUDGE_BASE_URL:-}"
JUDGE_KEY="${JUDGE_KEY:-}"
JUDGE_CONCURRENCY="${JUDGE_CONCURRENCY:-1}"
JUDGE_TEMPERATURE="${JUDGE_TEMPERATURE:-0}"
JUDGE_RETRY="${JUDGE_RETRY:-}"
JUDGE_WAIT="${JUDGE_WAIT:-}"

# --- output dir (kept distinct per subset so subset/full results don't collide) ---
case "$SUBSET" in
  full)   WORK_DIR="${WORK_DIR:-$REPO_DIR/eval_outputs/full}";;
  head)   WORK_DIR="${WORK_DIR:-$REPO_DIR/eval_outputs/head_${FRAC:-$N}}";;
  random) WORK_DIR="${WORK_DIR:-$REPO_DIR/eval_outputs/random_${FRAC:-$N}_seed${SEED}}";;
  indices) WORK_DIR="${WORK_DIR:-$REPO_DIR/eval_outputs/indices}";;
  *) echo "[run-eval] ERROR: SUBSET must be full|head|random|indices (got '$SUBSET')" >&2; exit 1;;
esac

if [ ! -d "$MODEL_PATH" ]; then
  echo "[run-eval] ERROR: MODEL_PATH '$MODEL_PATH' not found. Run 01_download_model.sh first." >&2
  exit 1
fi
if [ ! -f "$EVAL_DIR/run_monet.py" ]; then
  echo "[run-eval] ERROR: VLMEvalKit not set up. Run 03_setup_eval.sh first." >&2
  exit 1
fi
if [ "$SUBSET" = "head" ] || [ "$SUBSET" = "random" ]; then
  if { [ -n "$FRAC" ] && [ -n "$N" ]; } || { [ -z "$FRAC" ] && [ -z "$N" ]; }; then
    echo "[run-eval] ERROR: for SUBSET=$SUBSET set exactly one of FRAC or N." >&2; exit 1
  fi
fi
if [ "$SUBSET" = "indices" ]; then
  if [ -z "$INDICES_FILE" ] || [ ! -f "$INDICES_FILE" ]; then
    echo "[run-eval] ERROR: SUBSET=indices requires an existing INDICES_FILE" >&2
    exit 1
  fi
  if [ -n "$FRAC" ] || [ -n "$N" ]; then
    echo "[run-eval] ERROR: SUBSET=indices cannot be combined with FRAC or N" >&2
    exit 1
  fi
  INDICES_FILE="$(realpath -m "$INDICES_FILE")"
fi

# Refresh tracked Monet integration files on every run. 03_setup_eval.sh is still
# required once for installation and the checked inference hook, but subsequent
# code changes should not require rerunning the dependency installation step.
mkdir -p "$EVAL_DIR/Monet_models"
cp "$REPO_DIR/inference/vllm/monet_gpu_model_runner.py" "$EVAL_DIR/Monet_models/"
cp "$REPO_DIR/inference/vllm/latent_policy_logits.py" "$EVAL_DIR/Monet_models/"
cp "$REPO_DIR/run_scripts/vlmeval_run_monet.py" "$EVAL_DIR/run_monet.py"
cp "$REPO_DIR/run_scripts/latent_activation.py" "$EVAL_DIR/latent_activation.py"
cp "$REPO_DIR/run_scripts/latent_policy.py" "$EVAL_DIR/latent_policy.py"
cp "$REPO_DIR/run_scripts/secret_redaction.py" "$EVAL_DIR/secret_redaction.py"

cd "$EVAL_DIR"
# Make sitecustomize.py importable from the very first interpreter byte (parent + workers).
export PYTHONPATH="$EVAL_DIR:${PYTHONPATH:-}"

echo "[run-eval] model=$MODEL_PATH  datasets='$DATASETS'  subset=$SUBSET  frac=${FRAC:-} n=${N:-} seed=$SEED"
echo "[run-eval] work-dir=$WORK_DIR"
if [ -n "$MONET_LATENT_POLICY_MANIFEST" ]; then
  echo "[run-eval] latent-policy=$MONET_LATENT_POLICY_MANIFEST  reuse=$REUSE"
fi

# --- prepare each dataset's TSV (restore to full, then subset if requested) -
for d in $DATASETS; do
  python "$REPO_DIR/run_scripts/eval_subset.py" --dataset "$d" --mode restore
  if [ "$SUBSET" = "head" ] || [ "$SUBSET" = "random" ]; then
    SIZE_ARGS=(); [ -n "$FRAC" ] && SIZE_ARGS+=(--frac "$FRAC"); [ -n "$N" ] && SIZE_ARGS+=(--n "$N")
    python "$REPO_DIR/run_scripts/eval_subset.py" --dataset "$d" --mode "$SUBSET" --seed "$SEED" "${SIZE_ARGS[@]}"
  elif [ "$SUBSET" = "indices" ]; then
    python "$REPO_DIR/run_scripts/eval_subset.py" --dataset "$d" --mode indices \
      --indices-file "$INDICES_FILE"
  fi
done

# --- run evaluation --------------------------------------------------------
JUDGE_ARGS=()
if [ -n "$JUDGE" ]; then
  JUDGE_ARGS+=(--judge "$JUDGE")
  [ -n "$JUDGE_BASE_URL" ] && JUDGE_ARGS+=(--judge-base-url "$JUDGE_BASE_URL")
  [ -n "$JUDGE_KEY" ]      && JUDGE_ARGS+=(--judge-key "$JUDGE_KEY")
  JUDGE_ARGS+=(--judge-api-nproc "$JUDGE_CONCURRENCY")
  [ -n "$JUDGE_RETRY" ] && JUDGE_ARGS+=(--judge-retry "$JUDGE_RETRY")
  JUDGE_JSON="{\"temperature\": $JUDGE_TEMPERATURE"
  [ -n "$JUDGE_WAIT" ] && JUDGE_JSON+=", \"wait\": $JUDGE_WAIT"
  JUDGE_JSON+="}"
  JUDGE_ARGS+=(--judge-args "$JUDGE_JSON")
else
  echo "[run-eval] WARNING: no JUDGE set. README recommends an API judge for accurate scoring."
fi

RUN_ARGS=(
  --model Monet
  --data $DATASETS
  --work-dir "$WORK_DIR"
)
[ "$REUSE" = "1" ] && RUN_ARGS+=(--reuse)
RUN_ARGS+=("${JUDGE_ARGS[@]}")
python run_monet.py "${RUN_ARGS[@]}"

echo
echo "[run-eval] DONE. Results under: $WORK_DIR/Monet/"
